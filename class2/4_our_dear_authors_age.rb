# If I am 1,160 million seconds old (which I am, though I was somewhere in the
# 800 millions when I started this book), how old am I?

authors_age_seconds = 1160000000
seconds_in_minute = 60
minutes_in_hour = 60
hours_in_day = 24
days_in_a_year = 365
authors_age_in_years = authors_age_seconds / seconds_in_minute / minutes_in_hour / hours_in_day / days_in_a_year
puts authors_age_in_years
